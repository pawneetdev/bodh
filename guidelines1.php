<center><font style = "font-size: 30px; font-weight: bold;">Author Guidelines</font></center><br><br>

<b><u><font style = "font-size: 22px;">Abstracts</font></u></b><br>
The key arguments and findings should be outlined in an abstract of not more than 200 words.<br><br>

<b><u><font style = "font-size: 22px;;">Articles</font></u></b><br>
<font style="text-align:justify;">The journal publishes articles that make an original and critical contribution of a theoretical, or a more
empirical nature, to technology and management research. The journal will not discriminate between qualitative
and quantitative analyses, insofar as the article engages adequately with the literature in the relevant field, but
also with the broader interdisciplinary field.</font><br><br>

Articles normally don't exceed 8,000-10,000 words including references. When submitting an article, authors
should accept the following conditions:<br><br>
<ul style = "list-style-type: square;">
<li style="line-height:30px;">The work in the paper is not published in any other form (book, book chapter, part of a book, etc.) or (normally)
any other language. We do not accept book summaries. If the work is published in part,or wholly, previously in a 
language other than English, you will need to declare this. In this case,it is up to the Editors' judgement to 
proceed with the review process, or to reject the paper as non-original work.</li>
<li>The paper is not being submitted to any other journal simultaneously.</li>
<li>The author is fully authorized to submit the material for publication.</li>
<li>The author agrees to act as a referee for other submissions to the Journal.</li>
<li>If accepted, the paper will not be republished without the consent of the publishers.</li></ul>
<br><br>
<b><u><font style = "font-size: 22px;">References</font></u></b>
<br><br>
<i><font style = "font-size: 20px;">Citation in text</i></font><br>
Please ensure that every reference cited in the text is also present in the reference list (and vice versa). Any 
references cited in the abstract must be given in full. Unpublished results and personal communications are not 
recommended in the reference list, but may be mentioned in the text. If these references are included in the 
reference list they should follow the standard reference style of the journal and should include a substitution of 
the publication date with either 'Unpublished results' or 'Personal communication'. Citation of a reference as 
'in press' implies that the item has been accepted for publication.
<br><br>
<i><font style = "font-size: 20px;">Web references</i></font><br>
As a minimum, the full URL should be given and the date when the reference was last accessed. Any further
information, if known (DOI, author names, dates, reference to a source publication, etc.), should also be given. 
Web references can be listed separately (e.g., after the reference list) under a different heading if desired, or 
can be included in the reference list.<br><br>
