<i><font style = "font-size: 20px;">References in a special issue</i></font><br>
Please ensure that the words 'this issue' are added to any references in the list (and any citations in the text) to 
other articles in the same Special Issue.
<br><br>
<i><font style = "font-size: 20px;">Reference formatting</i></font><br>
There are no strict requirements on reference formatting at submission. References can be in any style or format
as long as the style is consistent. Where applicable, author(s) name(s), journal title/book title, chapter title/
article title, year of publication, volume number/book chapter and the pagination must be present. Use of DOI is 
highly encouraged. The reference style used by the journal will be applied to the accepted article by Elsevier at 
the proof stage. Note that missing data will be highlighted at proof stage for the author to correct. If you do 
wish to format the references yourself they should be arranged according to the following examples:
<br><br>
<i><font style = "font-size: 20px;">Reference style</i></font><br>
Text: Citations in the text should follow the referencing style used by the American Psychological Association.
You are referred to the Publication Manual of the American Psychological Association, Sixth Edition, 
ISBN 978-1-4338-0561-5, copies of which may be ordered from http://books.apa.org/books.cfm?id=4200067 
or APA Order Dept., P.O.B. 2710, Hyattsville, MD 20784, USA or APA, 3 Henrietta Street, London, WC3E 8LU, UK. 
<br>
List: references should be arranged first alphabetically and then further sorted chronologically if necessary. 
More than one reference from the same author(s) in the same year must be identified by the letters 'a', 'b', 'c', 
etc., placed after the year of publication. 
<br><br>
<i><font style = "font-size: 20px;">Examples:</i></font><br>
Reference to a journal publication: <br>
Van der Geer, J., Hanraads, J. A. J., & Lupton, R. A. (2010). The art of writing a scientific article. Journal 
of Scientific Communications, 163, 51-59.<br> 
Reference to a book: <br>
Strunk, W., Jr., & White, E. B. (2000). The elements of style. (4th ed.). New York: Longman, (Chapter 4).<br> 
Reference to a chapter in an edited book: <br>
Mettam, G. R., & Adams, L. B. (2009). How to prepare an electronic version of your article. In B. S. Jones, & R. Z. 
Smith (Eds.), Introduction to the electronic age (pp. 281â€“304). New York: E-Publishing Inc.<br>
